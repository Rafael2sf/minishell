#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	if (*lst)
		(new->n) = (*lst);
	else
		(new->n) = NULL;
	(*lst) = new;
}
